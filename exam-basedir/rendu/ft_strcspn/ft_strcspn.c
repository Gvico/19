#include <string.h>

size_t	ft_strcspn(const char *s1, const char *s2)
{
	size_t i;
	size_t j;

	i = 0;
	while (s1[i] != '\0')
	{
		j = 0;
		while (s2[j] != '\0' && s2[j] != s1[i])
			j++;
		if (s2[j] == s1[i])
			return (i);
		else
			i++;
	}
	return (i);
}

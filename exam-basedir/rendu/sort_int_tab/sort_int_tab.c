void	sort_int_tab(int *tab, unsigned int size)
{
	int				buf;
	unsigned int	i;

	i = 0;
	while (i + 1 < size)
	{
		if (tab[i] > tab[i + 1])
		{
			buf = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = buf;
			i = -1;
		}
		i++;
	}
}
